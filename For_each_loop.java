class For_each_loop{
    public static void main(String arg[])
        {
        String s[]={"one","tow","three"};
        int num[]={1,3,4,56,6};
        for(int var:num)
        {
            System.out.println(var);
        }
        for(String var:s)
        {
            System.out.println(var);
        }
        }
}